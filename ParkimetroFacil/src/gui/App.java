    private void btnBuscarPatenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPatenteActionPerformed
        // busca por patente (última coincidencia) para guardar datos de salida del vehiculo
        // debe buscar patente y verificar que no exista un registro duplicado en la tabla salida con el id de entrada. OJO
        GestionMinutos gestionMin = new GestionMinutos();
        try{
            CrudSalida crudSalida = new CrudSalida();
            Salida salida = new Salida();
            Entrada entrada = new Entrada();              
            String patente = txtPatenteSalida.getText();
            entrada = crudSalida.buscarEntrada(patente);            
            if (entrada.getPatente()!=null)
            {
                
                if ((txtPatenteSalida.getText()).trim().length()>=5 && (txtPatenteSalida.getText()).trim().length()<=7)
                {
                    salida.setId_entrada(entrada.getId());
                    salida.setHora_salida(lblHoraSalida.getText().trim().substring(0,5));
                    salida.setFecha_salida(lblFechaSalida.getText());            
                    if (crudSalida.insertarSalida(salida))
                    {                        
                        crudSalida.agregarEstacionameiento();
                        CrudEntrada crudEntrada = new CrudEntrada();
                        if (!crudEntrada.cambiarEstado(patente))
                        {
                            JOptionPane.showMessageDialog(null, "No existe esa patente para registrar salida");
                        }
                        //long minutos = gestionMin.minutosEstacionado(entrada.getHora(), salida.getHora_salida());
                        Detalle detalle = new Detalle(entrada, salida);
                        detallePago = detalle;
                        txaDetalle.setText(detalle.informacionEstacionamiento());
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Ocurrió un error: Salida No Ingresada");
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Debe ingresar una patente correcta");
                }
            }else{
                JOptionPane.showMessageDialog(null,"No existe esa patente para registrar salida");
            }
        }catch(Exception e)
        {
             JOptionPane.showMessageDialog(null,"Ocurrió un error:"+e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarPatenteActionPerformed

    
