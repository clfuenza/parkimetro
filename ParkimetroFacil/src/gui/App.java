    private void btnGuardarEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEntradaActionPerformed
        try{
            Entrada e = new Entrada();            
            CrudEntrada crudEntrada = new CrudEntrada();
            if ((txtPatenteEntrada.getText()).trim().length()>=5 && (txtPatenteEntrada.getText()).trim().length()<=7)
            {
                e.setFecha(lblFechaEntrada.getText());
                e.setHora(lblHoraEntrada.getText().substring(0,5));
                e.setPatente(txtPatenteEntrada.getText());
                e.setTipo(cboTipoVehiculo.getSelectedItem().toString());
                e.setEstacionado(true);
                
                if (!crudEntrada.entradaDuplicada(txtPatenteEntrada.getText()))
                {
                    if (crudEntrada.insertar(e))
                    {                     
                        JOptionPane.showMessageDialog(null,"Vehiculo Ingresado");
                        crudEntrada.restarEstacionamiento(); 
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Ocurrión un error: Vehiculo No ingresado");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null,"Esa patente ya existe como ingresada. Intente con otra.");
                }
                          
                int cantidadDisponible = crudEntrada.estacionamientosDisponibles();
                lblEstacionamientosDisponibles.setText(String.valueOf(cantidadDisponible));
                limpiarEntrada();
            }
            else{
              JOptionPane.showMessageDialog(null,"DEBE INGRESAR UNA PATENTE CORRECTA");
            }
            txtPatenteEntrada.requestFocus();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Ocurrió un error:"+e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarEntradaActionPerformed

